import dayjs from "dayjs";
export const generateBankData = ({
  openingBalance = 50000,
  salaryAmount = 65000,
  totalRows = 236
}) => {

  let balance = openingBalance;
  let rows = [];
  let lastSalaryMonth = null;

  for (let i = 0; i < totalRows; i++) {

    const date = dayjs().subtract(i, "days");
    const day = date.date();
    const month = date.format("YYYY-MM");

    let debit = null;
    let credit = null;

    // Salary only once per month (5 or 6)
    if ((day === 5 || day === 6) && lastSalaryMonth !== month) {
      credit = salaryAmount;
      balance += credit;
      lastSalaryMonth = month;
    }

    // Interest example
    else if (i % 28 === 0 && i !== 0) {
      credit = Number((Math.random() * 200 + 100).toFixed(2));
      balance += credit;
    }

    // Normal debit
    else {
      const debitOptions = [
        100, 200, 300, 450, 580, 770, 920,
        1100, 1300, 1500, 1700, 2000, 2400
      ];

      debit = debitOptions[Math.floor(Math.random() * debitOptions.length)];
      balance -= debit;
    }

    rows.push({
      debit,
      credit,
      balance: Number(balance.toFixed(2))
    });
  }

  return rows;
};

